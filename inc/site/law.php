			<TD align=justify><b>Статья 1. <u>Флуд</u></b><br>
			<b>1.Флуд</b> - Повторение однотипного сообщения в чате чаще, чем
			один раз в три минуты. Наказывается предупреждением, при повторных
			нарушениях запретом общения на 15 минут. Злостный флуд, т.е.
			загромождение чатового окна одинаковыми сообщениями или смайлами,
			которые приводят к сбою в его работе наказывается чатовым молчанием
			на 60 минут. <BR>
			<b>2. Спам</b> - Отправка в чат бессмысленного набора букв, символов,
			цифр, смайлов. Сообщения не несущие смысловой нагрузки. Наказывается
			предупреждением, при повторном нарушении запретом общения на 15
			минут. <BR>
			<u>Под словом "флуд" понимается:</u> <BR>
			- сообщения, не несущие смысловой нагрузки; <BR>
			- одинаковые (однородные) сообщения, пускаемые в чат чаще, чем один
			раз в три минуты; <BR>
			- бессмысленные наборы символов и смайлов; <BR>
			- сообщения на форуме в служебных разделах, не по форме. <BR>
			<b><small>(Важно: Форма и правила пользования служебными разделами
			форума указаны на первых страницах этих разделов).</small></b> <BR>
			<b>3. Флуд на форуме</b>, т.е. опубликование бессмысленных сообщений
			состоящих из одних междометий, местоимений, знаков препинания и т.д.,
			наказывается блокированием форумного аккаунта на сутки и удалением
			сообщения или топика. <BR>
			<u>Примечание к уложению:</u> <BR>
			Администрация и Инквизиция имеют право на сообщения, написанные
			ЗАГЛАВНЫМИ буквами.<br>
			<br>
			<br>
			<B>Статья 2. <u>Иностранные языки</u></B><br>
			<b>1. Использование слов и выражений на языках отличных от русского в
			чате</b>, наказывается предупреждением или запретом чатового общения
			на 15 минут. <BR>
			<b>2. Использование слов и выражений на языках отличных от русского
			на форуме</b>, наказывается блокированием форумного аккаунта на сутки
			и удалением сообщения или топика. <BR>
			Исключения: <BR>
			- ник персонажа; <BR>
			- текст в инфе персонажа; <BR>
			- цитаты при обязательном указании автора цитаты <BR>
			(Пример: "To be, or not to be..."©Шекспир), а также общеизвестные
			выражения на языке оригинала (Пример: "Post Scriptum" или сокращенно
			"P.S." (С латыни - Приписка после основного текста)).<br>
			<br>
			<br>
			<B>Статья 3. <u>Попрошайничество</u></b> <br>
			<b>1. Попрошайничество</b>, т.е. просьба, обращённая к игрокам в чате
			о безвозмездной помощи денежными единицами, вещами Падшие Ангелы, и
			не только, предупреждается, либо наказывается запретом чатового
			общения на 15 минут. <BR>
			<b>2. Попрошайничество</b>, т.е. просьба, обращённая к игрокам на
			форуме о безвозмездной помощи денежными единицами, <BR>
			вещами Падшие Ангелы и не только, наказывается блокированием
			форумного аккаунта на сутки и удалением сообщения или топика. <br>
			<br>
			<br>
			<b>Статья 4. <u>Распространение ссылок</u></b><br>
			<b>1. Распространение вредоносных ссылок (РВС)</b> или указание
			порядка действий, приводящего в конечном итоге к вредоносной ссылке.
			<BR>
			Наказание: применение молчанки на срок сутки, заведение дела по факту
			РВС и передача его ОП. <BR>
			Важно: РВС - ссылка, направленная на причинение ущерба игрокам
			(попытка узнать пароль, взлом и т.п.). <BR>
			Любые другие ссылки под эту статью не подпадают! <BR>
			<b>2. Распространение ссылок на ресурсы</b>, переход на которые по
			распространяемой ссылке приводит к зарабатыванию распространителем
			реальных и/или игровых денег. <br>
			Наказание: применение молчанки на срок сутки, заведение дела и
			передача его ОП.<BR>
			<b>3. Любые другие интернет ссылки</b> <BR>
			Наказание: применение молчанки на срок 30 минут. <BR>
			Исключения: <BR>
			- приватный чат; <BR>
			- информация о персонаже; <BR>
			- разделы форума, разрешающие ссылки на другие интернет сайты.<br>
