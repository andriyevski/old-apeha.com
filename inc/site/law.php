			<b>Статья 5. <u>Нецензурные выражения</u></b> <br>
			<b>1. Использование нецензурных выражений</b>, в прямом виде или
			завуалированной форме, в чате, наказывается запретом чатового общения
			на 60 минут. <BR>
			<b>2. Использование нецензурных выражений</b>, в прямом виде или
			завуалированной форме, на форуме, наказывается блокированием
			форумного аккаунта на 3 суток, удалением сообщения или топика.<br>
