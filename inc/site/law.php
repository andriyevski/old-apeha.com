			<b>Статья 15. <u>Мультоводство</u></b><br>
			<b>1.Заперещена передача персонажа</b>, а также доступа к нему,
			третьему лицу. <BR>
			<b>2. Мульты</b> - персонажи, имеющие одинаковые ip-адреса и
			совместные бои с одинаковым исходом более 10. <BR>
			<b>3. Мульты-боты</b> - персонажи, имеющие одинаковые ip-адреса и
			явные признаки одного владельца: частично совпадающие или
			тематические ники, аналогичные данные информации персонажа. <BR>
			<b>4. Финансовые боты</b> - мульты, созданные мультоводом для
			финансовой прокачки. <BR>
			<b>5. Скан паспорта</b> - отсканированный лист паспорта владельца
			персонажа. Скан нужен для того, чтобы владелец смог доказать, что
			персонаж действительно принадлежит ему в случае взлома или подозрения
			в мультоводстве. Выявление у персонажа мультов-ботов наказывается: -
			Заведением дела и отправления в тюрьму с последующей блокировкой
			персонажа.<br>
