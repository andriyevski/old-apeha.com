			<b>Статья 6. <u>Оскорбление</u></b><br>
			<b>1. Оскорбление др. игрока</b>, а так же безадресное, в общем чате
			выражениями, в том числе на других языках, унижающими честь и
			достоинство человека, наказывается запретом чатового общения на 60
			минут. <BR>
			<b>2. Оскорбление проекта</b>, администрации проекта или Инквизитора
			в чате, выражениями, в том числе на других языках, унижающими честь и
			достоинство человека, наказывается запретом чатового общения на
			сутки. <BR>
			<b>3. Оскорбление игрока на форуме выражениями</b>, в том числе на
			других языках, унижающими честь и достоинство человека, наказывается
			блокированием форумного аккаунта на 7 суток, удалением сообщения или
			топика. <BR>
			<b>4. Оскорбление проекта</b>, администрации проекта или Инквизиции
			на форуме, выражениями, в том числе на других языках, унижающими
			честь и достоинство, наказывается Верховным Инквизитором,
			блокированием форумного аккаунта на 10 суток, удалением сообщения или
			топика.<br>
