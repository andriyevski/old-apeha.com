<p><a id="11"><b><span lang="ru">10</span>. Действия персонажа</b></a></p>
<p align="justify"><a id="11-1"><b>Q: Как пользоваться рюкзаком?</b></a><br>

A: Для того чтобы воспользоваться рюкзаком нажмите на кнопку “<span
	lang="ru">Настройки / Инвентарь</span>”. В <span lang="ru">инвентаре</span>
вы можете увидеть вещи, которые есть у Вас, надеть их, снять или
выбросить.</p>
<p align="justify"><a id="11-2"><b>Q: Могу ли я передать вещь или деньги
другу?</b></a><br>
A: Да, если вы <span lang="ru">достигли 4 уровня.При условии что деньги
даються в долг, а вещи на продажу. В противном случае это считаеться
подозрительной передачей, если только вы не состоите в одном клане.</span></p>
<p align="justify"><a id="11-4"><b>Q: Как купить вещь и куда я могу ее
продать?</b></a><br>

A: Вещи покупаются в магазине,<span lang="ru"> там же их можно продать.</span></p>

